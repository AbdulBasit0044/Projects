                <h2>Large Heading</h2>

                <p>In many ways, Pakistan’s rise from No. 8 in the ICC ODI rankings to its maiden ICC Champions Trophy title captured the very essence of the tournament. At almost every press conference leading up to the tournament, the general consensus was that the Champions Trophy was one tournament where there were no favourites, and that all teams had an equal chance.</p>

                <p>As things turned out, it wasn’t just lip service, the most incredible example being the Pakistan story. After a demoralising loss to India in its opening game, the least fancied and lowest ranked team in the competition stunned South Africa and sneaked past Sri Lanka to make it to the knockout stages. If that was impressive, what followed was staggering: Pakistan destroyed England and India in its next two matches to complete its dream run.</p>

                <p>But the Pakistan fairytale was just one part of the story. The ICC Champions Trophy 2017 showed that it is a great leveller as there were plenty more unpredictable results. Bangladesh beating New Zealand to make it to its first semi-final in an ICC event, and Sri Lanka smashing India in the group stage proved that the gap between the sides is not too wide on a given day.</p>

                <p>The tournament also surprised many in that it didn’t turn out to be a batsman v batsman contest. There was speculation ahead of the start that bowlers would have tough times on flat pitches, but ironically, the sides that made it to the final were the ones that had the best bowling attacks.</p>

                <p>Scores in excess of 350 were expected to be the norm, but the highest score in the ICC Champions Trophy 2017 was 338, which too came as late as the final. In fact, the Player of the Tournament was Hasan Ali, a bowler.
                </p>

                

