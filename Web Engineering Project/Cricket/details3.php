                <p>The IPL has enjoyed another highly successful season featuring numerous nail-biting last-over finishes and a tension-building scramble among teams vying for a place in the finals</p>.

<p>In its 11th season, the tournament has certainly not lost, and if anything only gained in, lustre. Players from all around the cricketing world are desperate to win a contract and become involved in what is widely considered the most glamorous and definitely the most rewarding of all the T20 leagues.</p>

<p>In addition to the financial benefits the IPL has brought Indian cricket, the league has had a huge positive effect on playing standards. Since the advent of the IPL, Indian national teams have been more confident and aggressive in their approach to the game, and nowhere does this better show up than in their fielding.</p>

<p>The overseas influence on Indian cricket through the IPL has resulted in a more combative national team, confident in its ability to match it with any opposition. There's nothing like playing with and against international stars on a regular basis to help young players discover that despite their great skill, these lauded individuals are still vulnerable to a good delivery or capable of bowling the odd bad ball.</p>

<p>Where a young Indian player in the past often lusted after a meeting with his idol from overseas, now they dream of replicating the deeds of Virat Kohli and MS Dhoni.</p>

